<h3> À propos </h3><br>


<div class= "para">

En français<br>
<br>
<p>L’idée de ce site internet m’est venue au début de mes recherches pour mon doctorat. Je travaille sur les récits d’esclaves publiés avant la guerre de Sécession et je me suis vite rendu compte que la liste des récits ne fait pas consensus parmi les chercheurs et ceux qui en proposent une liste n’expliquent pas vraiment sur quels critères les récits sont inclus et les listes diffèrent entre elles. Le but de ce site est donc de présenter une liste la plus exhaustive possible des récits qui sont cités par les différents chercheurs mais avec des commentaires sur ce qui fait qu’ils sont (ou pas) des récits d’esclaves. Pour l’instant nous nous concentrons sur les récits écrits avant la guerre de Sécession mais il n’est pas exclu que le site évolue et qu’il englobe également les récits écrits après. </p><br>
<p>Pour toute remarque, questions ou si vous notez une erreur, merci de m’écrire à recits-esclaves@univ-tlse2.fr<br>
Marie-Pierre Baduel<br>
Laboratoire CAS (Cultures AnglophoneS)<br>
Université Toulouse Jean Jaurès<br>
</p>

In English<br>
<br>
<p>The idea for this website came to me as I was beginning my research for my PhD. I work on slave narratives published before the Civil War and I quickly realized that no two researchers agree on what a slave narrative is and that those who make a list do not really explain what criteria they used to make up this list and as a consequence, the lists are different from one researcher to the next. The aim of this website is thus to give a list, as thorough as possible, of the narratives alluded to by researchers but with comments on what makes them (or do not make them) slave narratives. For the moment we focus on the narratives written before the Civil War but including also the narratives written after the Civil War could be considered in the future.   
<p>If you notice a mistake or if you have a comment, please write to<br> recits-esclaves@univ-tlse2.fr<br>
Marie-Pierre Baduel<br>
Laboratoire CAS (Cultures AnglophoneS)<br>
Université Toulouse Jean Jaurès<br>
</p>
